import UIKit
extension UIColor {
    func tabBarTintColor() -> UIColor {
        return UIColor.blue
    }
}
extension UIColor {
    static func candyGreen() -> UIColor {
        return UIColor(red: 67.0/255.0, green: 205.0/255.0, blue: 135.0/255.0, alpha: 1.0)
    }
    static func facebookBlue() -> UIColor {
        return UIColor(red: 94.0/255.0, green: 149.0/255.0, blue: 251.0/255.0, alpha: 1.0)
    }
}
